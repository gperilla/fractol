char	*ft_strcat(char *s1, const char *s2)
{
	char		*d1;
	const char	*d2;

	d1 = s1;
	d2 = s2;
	while (*d1)
		d1++;
	while (*d2)
		*d1++ = *d2++;
	*d1 = '\0';
	return (s1);
}
