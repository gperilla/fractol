#include <stdlib.h>

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int i;

	i = 0;
	if (f != NULL)
	{
		while (s[i])
		{
			(*f)(&s[i]);
			i++;
		}
	}
}
