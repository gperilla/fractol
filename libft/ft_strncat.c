#include <stdlib.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char		*d1;
	const char	*d2;

	d1 = s1;
	d2 = s2;
	while (*d1)
		d1++;
	while (*d2 && n--)
		*d1++ = *d2++;
	*d1 = '\0';
	return (s1);
}
