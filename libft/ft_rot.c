#include "libft.h"

void	ft_rot(char *str, int n)
{
	int i;

	n %= 26;
	i = 0;
	while (str[i])
	{
		if (ft_isupper(str[i]))
			str[i] = ((str[i] - 'A' + n) % 26) + 'A';
		else if (ft_islower(str[i]))
			str[i] = ((str[i] - 'a' + n) % 26) + 'a';
		i++;
	}
}
